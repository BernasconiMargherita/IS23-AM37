package it.polimi.ingsw.model;

public class Coordinates {
    private final Integer X;
    private final Integer Y;

    public Coordinates(Integer X, Integer Y) {
        this.X = X;
        this.Y = Y;
    }

    public Integer getX() {
        return X;
    }

    public Integer getY() {
        return Y;
    }
}
